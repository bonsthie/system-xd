const os = @import("std").os.linux;

pub fn errnoToError(err: usize) anyerror!usize {
    return switch (os.E.init(err)) {
        os.E.SUCCESS => @as(usize, 0),
        os.E.PERM => error.NoPermission,
        os.E.NOENT => error.NoEntry,
        os.E.SRCH => error.NoEntry,
        os.E.INTR => error.Interrupted,
        os.E.IO => error.InputOutput,
        os.E.NXIO => error.NoSuchDeviceOrAddress,
        os.E.@"2BIG" => error.FileTooBig,
        os.E.NOEXEC => error.NoExecute,
        os.E.BADF => error.BadFileDescriptor,
        os.E.CHILD => error.NoChildProcess,
        os.E.AGAIN => error.TryAgain,
        os.E.NOMEM => error.OutOfMemory,
        os.E.ACCES => error.PermissionDenied,
        os.E.FAULT => error.BadAddress,
        os.E.NOTBLK => error.NotBlockDevice,
        os.E.BUSY => error.DeviceOrResourceBusy,
        os.E.EXIST => error.FileExists,
        os.E.XDEV => error.CrossDeviceLink,
        os.E.NODEV => error.NoDevice,
        os.E.NOTDIR => error.NotDirectory,
        os.E.ISDIR => error.IsDirectory,
        os.E.INVAL => error.InvalidArgument,
        os.E.NFILE => error.TooManyOpenFiles,
        os.E.MFILE => error.TooManyOpenFiles,
        os.E.NOTTY => error.NotATypewriter,
        os.E.TXTBSY => error.TextFileBusy,
        os.E.FBIG => error.FileTooBig,
        os.E.NOSPC => error.NoSpaceLeftOnDevice,
        os.E.SPIPE => error.IllegalSeek,
        os.E.ROFS => error.ReadOnlyFileSystem,
        os.E.MLINK => error.TooManyLinks,
        os.E.PIPE => error.BrokenPipe,
        os.E.DOM => error.MathArgumentOutOfDomain,
        os.E.RANGE => error.MathResultNotRepresentable,
        os.E.DEADLK => error.ResourceDeadlockWouldOccur,
        os.E.NAMETOOLONG => error.FileNameTooLong,
        os.E.NOLCK => error.NoLocksAvailable,
        os.E.NOSYS => error.FunctionNotImplemented,
        os.E.NOTEMPTY => error.DirectoryNotEmpty,
        os.E.LOOP => error.TooManyLevelsOfSymbolicLinks,
        os.E.NOMSG => error.NoMessageOfDesiredType,
        os.E.IDRM => error.IdentifierRemoved,
        os.E.CHRNG => error.ChannelNumberOutOfRange,
        os.E.L2NSYNC => error.Level2NotSynchronized,
        os.E.L3HLT => error.Level3Halted,
        os.E.L3RST => error.Level3Reset,
        os.E.LNRNG => error.LinkNumberOutOfRange,
        os.E.UNATCH => error.ProtocolDriverNotAttached,
        os.E.NOCSI => error.NoCSIOperation,
        os.E.L2HLT => error.Level2Halted,
        os.E.BADE => error.InvalidExchange,
        os.E.BADR => error.InvalidRequestDescriptor,
        os.E.XFULL => error.ExchangeFull,
        os.E.NOANO => error.NoANO,
        os.E.BADRQC => error.InvalidRequestCode,
        os.E.BADSLT => error.InvalidSlot,
        os.E.BFONT => error.BadFontFileFormat,
        os.E.NOSTR => error.DeviceNotAStream,
        os.E.NODATA => error.NoDataAvailable,
        os.E.TIME => error.TimerExpired,
        os.E.NOSR => error.OutOfStreamsResources,
        os.E.NONET => error.MachineIsNotOnTheNetwork,
        os.E.NOPKG => error.PackageNotInstalled,
        os.E.REMOTE => error.ObjectIsRemote,
        os.E.NOLINK => error.LinkHasBeenSevered,
        os.E.ADV => error.AdvertiseError,
        os.E.SRMNT => error.SrmountError,
        os.E.COMM => error.CommunicationErrorOnSend,
        os.E.PROTO => error.ProtocolError,
        os.E.MULTIHOP => error.MultihopAttempted,
        os.E.DOTDOT => error.RFSSpecificError,
        os.E.BADMSG => error.NotAMessage,
        os.E.OVERFLOW => error.ValueTooLargeForDefinedDataType,
        os.E.NOTUNIQ => error.NameNotUniqueOnNetwork,
        os.E.BADFD => error.FileDescriptorInBadState,
        os.E.REMCHG => error.RemoteAddressChanged,
        os.E.LIBACC => error.CanNotAccessADefinedSharedLibrary,
        os.E.LIBBAD => error.AccessingADefinedSharedLibrary,
        os.E.LIBSCN => error.LibSectionIsCorrupt,
        os.E.LIBMAX => error.AttemptingToLinkInTooManySharedLibraries,
        os.E.LIBEXEC => error.CannotExecADefinedSharedLibraryDirectly,
        os.E.ILSEQ => error.IllegalByteSequence,
        os.E.RESTART => error.InterruptedSystemCallShouldBeRestarted,
        os.E.STRPIPE => error.StreamsPipeError,
        os.E.USERS => error.TooManyUsers,
        os.E.NOTSOCK => error.SocketOperationOnNonSocket,
        os.E.DESTADDRREQ => error.DestinationAddressRequired,
        os.E.MSGSIZE => error.MessageTooLong,
        os.E.PROTOTYPE => error.ProtocolWrongTypeForSocket,
        os.E.NOPROTOOPT => error.ProtocolNotAvailable,
        os.E.PROTONOSUPPORT => error.ProtocolNotSupported,
        os.E.SOCKTNOSUPPORT => error.SocketTypeNotSupported,
        os.E.OPNOTSUPP => error.OperationNotSupported,
        os.E.PFNOSUPPORT => error.ProtocolFamilyNotSupported,
        os.E.AFNOSUPPORT => error.AddressFamilyNotSupportedByProtocol,
        os.E.ADDRINUSE => error.AddressAlreadyInUse,
        os.E.ADDRNOTAVAIL => error.CannotAssignRequestedAddress,
        os.E.NETDOWN => error.NetworkIsDown,
        os.E.NETUNREACH => error.NetworkIsUnreachable,
        os.E.NETRESET => error.NetworkDroppedConnectionBecauseReset,
        os.E.CONNABORTED => error.SoftwareCausedConnectionAbort,
        os.E.CONNRESET => error.ConnectionResetByPeer,
        os.E.NOBUFS => error.NoBufferSpaceAvailable,
        os.E.ISCONN => error.TransportEndpointIsAlreadyConnected,
        os.E.NOTCONN => error.TransportEndpointIsNotConnected,
        os.E.SHUTDOWN => error.CannotSendAfterTransportEndpointShutdown,
        os.E.TOOMANYREFS => error.TooManyReferences,
        os.E.TIMEDOUT => error.ConnectionTimedOut,
        os.E.CONNREFUSED => error.ConnectionRefused,
        os.E.HOSTDOWN => error.HostIsDown,
        os.E.HOSTUNREACH => error.NoRouteToHost,
        os.E.ALREADY => error.OperationAlreadyInProgress,
        os.E.INPROGRESS => error.OperationNowInProgress,
        os.E.STALE => error.StaleFileHandle,
        os.E.UCLEAN => error.StructureNeedsCleaning,
        os.E.NOTNAM => error.NotANXExtendedNameFile,
        os.E.NAVAIL => error.NoXExtendedSemanticsAvailable,
        os.E.ISNAM => error.IsANameFile,
        os.E.REMOTEIO => error.RemoteIOError,
        os.E.DQUOT => error.DiskQuotaExceeded,
        os.E.NOMEDIUM => error.NoMediumFound,
        os.E.MEDIUMTYPE => error.WrongMediumType,
        os.E.CANCELED => error.OperationCanceled,
        os.E.NOKEY => error.RequiredKeyNotAvailable,
        os.E.KEYEXPIRED => error.KeyHasExpired,
        os.E.KEYREVOKED => error.KeyHasBeenRevoked,
        os.E.KEYREJECTED => error.KeyWasRejectedByService,
        os.E.OWNERDEAD => error.OwnerDied,
        os.E.NOTRECOVERABLE => error.StateNotRecoverable,
        os.E.RFKILL => error.OperationNotPossibleDueToRFKill,
        os.E.HWPOISON => error.MemoryPageHasHardwareError,
        os.E.NSRNODATA => error.DNSServerReturnedAnswerWithNoData,
        os.E.NSRFORMERR => error.DNSServerClaimedQueryWasMisformatted,
        os.E.NSRSERVFAIL => error.DNSServerReturnedGeneralFailure,
        os.E.NSRNOTFOUND => error.DNSServerDidNotFindRecord,
        os.E.NSRNOTIMP => error.DNSServerDoesNotImplementRequestedOperation,
        os.E.NSRREFUSED => error.DNSServerRefusedQuery,
        os.E.NSRBADQUERY => error.MisformattedDNSQuery,
        os.E.NSRBADNAME => error.MisformattedDomainName,
        os.E.NSRBADFAMILY => error.UnsupportedAddressFamily,
        os.E.NSRBADRESP => error.MisformattedDNSResponse,
        os.E.NSRCONNREFUSED => error.CouldNotContactDNSServers,
        os.E.NSRTIMEOUT => error.TimeoutWhileContactingDNSServers,
        os.E.NSROF => error.EndOfFile,
        os.E.NSRFILE => error.ErrorReadingFile,
        os.E.NSRNOMEM => error.NoMemory,
        os.E.NSRDESTRUCTION => error.ApplicationTerminatedLookup,
        os.E.NSRQUERYDOMAINTOOLONG => error.DNSQueryReturnedTooManyRecords,
        os.E.NSRCNAMELOOP => error.DNSCanonicalNameLoop,
        _ => error.UnknownErrno,
    };
}
